local function AddSet(minVersion, maxVersion, collection, id, title, class, role, faction, ...)
	if WOW_VERSION >= minVersion then
		if maxVersion == nil or WOW_VERSION <= maxVersion then
			local tempSet = { }
			tempSet = {
				Title = title,
				Class = class.Description,
				Role = role.Description,
				Faction = faction.Description,
				Name = name,
				Variants = { }
			}
			local ID = collection.Code..string.format("%03d", id)..class.Code..role.Code..faction.Code
			SetCollectorDB[collection.ID].Sets[ID] = tempSet
			return ID
		end
	end
end

local function AddVariant(minVersion, maxVersion, collection, set, title, ...)
	if WOW_VERSION >= minVersion then
		if maxVersion == nil or WOW_VERSION <= maxVersion then
			local tempItems = { }
			local n = select("#",...)
			for i = 1,n do
				local v = select(i,...)
				tinsert(tempItems,v)
			end
			local tempVariant = {
				Title = title,
				Items = tempItems,
				Count = #tempItems
			}
			tinsert(SetCollectorDB[collection.ID].Sets[set].Variants, tempVariant)	
		end
	end	
end

local function T17(itemID, bonusID)
	local itemString = ""
	if itemID ~= nil and bonusID ~= nil then
		itemString = "item:"..itemID..":0:0:0:0:0:0:0:0:0:491:1:"..bonusID
	else
		itemString = itemID
	end
	return itemString
end

