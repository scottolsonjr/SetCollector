function SetCollectorSetupCharacterDB(class, release, override)
	if SetCollectorCharacterDB == nil or override == true then
		SetCollectorCharacterDB = { version = DB_VERSION, release = release, minimap = { hide = false }, Sets = { }, Items = { } }
		for i=1, #SetCollectorDB do
			for j in pairs(SetCollectorDB[i].Sets) do
				if ( SetCollectorDB[i].Sets[j].Class == class or SetCollectorDB[i].Sets[j].Class == ANY.Description ) then
					-- Set Info
					SetCollectorCharacterDB.Sets[j] = { Favorite = false, Tracking = false }
					-- Item Info
					for k=1, #SetCollectorDB[i].Sets[j].Variants do
						for l=1, #SetCollectorDB[i].Sets[j].Variants[k].Items do
							local itemID = SetCollectorDB[i].Sets[j].Variants[k].Items[l]
							local itemCheck = itemID
							SetCollectorCharacterDB.Items[itemID] = { Count = 0 }
							local count = SetCollectorCharacterDB.Items[itemID].Count
						end
					end
				end
			end
		end
		if (override == true) then print("|cffff6666".._L["CHAR_DB_UPGRADED"].."|r") end
		
	elseif SetCollectorCharacterDB.release < 30 then
		SetCollectorCharacterDB = { version = DB_VERSION, release = release, minimap = { hide = false }, Sets = { }, Items = { } }
		for i=1, #SetCollectorDB do
			for j in pairs(SetCollectorDB[i].Sets) do
				if ( SetCollectorDB[i].Sets[j].Class == class or SetCollectorDB[i].Sets[j].Class == ANY.Description ) then
					-- Set Info
					SetCollectorCharacterDB.Sets[j] = { Favorite = false, Tracking = false }
					-- Item Info
					for k=1, #SetCollectorDB[i].Sets[j].Variants do
						for l=1, #SetCollectorDB[i].Sets[j].Variants[k].Items do
							local itemID = SetCollectorDB[i].Sets[j].Variants[k].Items[l]
							SetCollectorCharacterDB.Items[itemID] = { Count = 0 }
						end
					end
				end
			end
		end
		print("|cff6666ff".._L["CHAR_DB_UPGRADED"].."|r")
		
	elseif SetCollectorCharacterDB.version ~= DB_VERSION or SetCollectorCharacterDB.release < release then
		SetCollectorCharacterDB.version = DB_VERSION
		SetCollectorCharacterDB.release = release
		local added = 0
		for i=1, #SetCollectorDB do
			for j in pairs(SetCollectorDB[i].Sets) do
				if ( SetCollectorDB[i].Sets[j].Class == class or SetCollectorDB[i].Sets[j].Class == ANY.Description ) then
					-- Set Info
					if ( not SetCollectorCharacterDB.Sets[j] ) then
						SetCollectorCharacterDB.Sets[j] = { Favorite = false, Tracking = false }
						added = added + 1
					end
					-- Item Info
					for k=1, #SetCollectorDB[i].Sets[j].Variants do
						for l=1, #SetCollectorDB[i].Sets[j].Variants[k].Items do
							local itemID = SetCollectorDB[i].Sets[j].Variants[k].Items[l]
							if ( not SetCollectorCharacterDB.Items[itemID] ) then
								SetCollectorCharacterDB.Items[itemID] = { Count = 0 }
								added = added + 1
							end
						end
					end
				end
			end
		end
		if ( added > 0 ) then
			print("|cff66ff66".._L["CHAR_DB_UPGRADED"].."|r")
		end
	end
	
