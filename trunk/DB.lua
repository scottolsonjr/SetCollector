local function GetCount(T)
  local count = 0
  for _ in pairs(T) do count = count + 1 end
  return count
end

local function AddCollection(minVersion, maxVersion, type, id, class, faction, role, name, minLvl, ...)
	if WOW_VERSION >= minVersion then
		if maxVersion == nil or WOW_VERSION <= maxVersion then
			local tempCollection = { }
			tempCollection = {
				Type = type.Description,
				Faction = faction.Description,
				Role = role.Description,
				Name = name,
				MinLevel = minLvl,
				Sets = { }
			}
			local ID = type.Code..string.format("%02d", id)..faction.Code..role.Code
			SetCollectorDB[class].Collections[ID] = tempCollection
			return ID
		end
	end
end

local function AddSet(minVersion, maxVersion, class, faction, collectionIndex, role, name, minLvl, iLvl, quality, ...)
	if WOW_VERSION >= minVersion then
		if maxVersion == nil or WOW_VERSION <= maxVersion then
			local tempSetPieces = { }
			local n = select("#",...)
			for i = 1,n do
				local v = select(i,...)
				tinsert(tempSetPieces,v)
			end
			local tempSet = {
				Faction = faction.Description,
				Role = role.Description,
				Name = name,
				MinLevel = minLvl,
				ItemLevel = iLvl,
				Quality = quality,
				setPieces = tempSetPieces,
				setPiecesNumAvailable = #tempSetPieces
			}
			tinsert(SetCollectorDB[class].Collections[collectionIndex].Sets, tempSet)	
		end
	end	
end

local function T17(itemID, bonusID)
	local itemString = ""
	if itemID ~= nil and bonusID ~= nil then
		itemString = "item:"..itemID..":0:0:0:0:0:0:0:0:0:491:1:"..bonusID
	else
		itemString = itemID
	end
	return itemString
end

